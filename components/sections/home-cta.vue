<script setup>
import { ref, onMounted, nextTick } from "vue";

const brandSlider = ref(null);

onMounted(async () => {
  // Attendre que le DOM soit complètement rendu
  await nextTick();

  // Initialisation explicite du slider après le montage du composant
  if (process.client && brandSlider.value) {
    // Forcer le rendu immédiat des images
    const images = brandSlider.value.querySelectorAll("img");
    images.forEach((img) => {
      if (img.complete) {
        img.style.opacity = "1";
      } else {
        img.addEventListener("load", () => {
          img.style.opacity = "1";
        });
      }
    });

    // Attendre que Swiper soit disponible
    const initSlider = () => {
      if (window.Swiper) {
        new window.Swiper(brandSlider.value, {
          loop: true,
          autoplay: {
            delay: 3000,
            disableOnInteraction: false,
          },
          breakpoints: {
            0: {
              slidesPerView: 1,
              spaceBetween: 20,
            },
            576: {
              slidesPerView: 2,
              centeredSlides: true,
              spaceBetween: 30,
            },
            768: {
              slidesPerView: 3,
              spaceBetween: 40,
            },
            1025: {
              slidesPerView: 4,
              spaceBetween: 50,
            },
            1400: {
              slidesPerView: 5,
              spaceBetween: 60,
            },
          },
        });
      } else {
        // Si Swiper n'est pas encore chargé, réessayer dans 100ms
        setTimeout(initSlider, 100);
      }
    };

    initSlider();
  }
});
</script>

